char	*ft_strrchr(const char *s, int c)
{
	int	i;
	int	tmp;

	i = 0;
	tmp = 0;
	while (s[i])
	{
		if (s[i] == (char)c)
		{
			tmp = i;
			i++;
		}
	}
	if (s[i] == (char)c)
		return (&s[i]);
	return(&s[tmp]);
}
